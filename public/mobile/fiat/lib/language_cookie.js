function setLocal(name, value) {
    window.localStorage.setItem(name, value)
}

function getLocal(name) {

    return window.localStorage.getItem(name) || '';
}

function changeLg() {
    var lang = '';
    if (getLocal('lang') && getLocal('lang') == 'zh') {
        lang = getLocal('lang')
    } else if (getLocal('lang') && getLocal('lang') == 'hk') {
        lang = getLocal('lang')
    } else if (getLocal('lang') && getLocal('lang') == 'kr') {
        lang = getLocal('lang')
    } else if (getLocal('lang') && getLocal('lang') == 'en') {
        lang = getLocal('lang')
    } else {
        lang = 'en';
        setLocal('lang', 'en');
    }

    var urls = JSON.stringify(window.location);
    if (urls.indexOf('black') != -1) {
        $("[data-localize]").localize("text", {
            pathPrefix: "../lang",
            language: lang
        });
    } else {
        $("[data-localize]").localize("text", {
            pathPrefix: "lang",
            language: lang
        });
    }
    // $("[data-localize]").localize("text", {
    //     pathPrefix: "lang",
    //     language: lang
    // });
    setLang(lang)

}

